import { readdir, readFile, writeFile } from "node:fs/promises";
import { join, relative, resolve } from "node:path";
import { LAYERS, toLayerOutputKey } from "../src/layers/registry";

const here = import.meta.dirname;
const packageRoot = resolve(here, "..");
const templatesDir = join(packageRoot, "templates");
const outFile = join(packageRoot, "src", "templates.generated.ts");

const listEntriesRecursive = async (dir: string, base: string): Promise<readonly string[]> => {
  const out: string[] = [];
  const entries = await readdir(dir, { withFileTypes: true });
  for (const e of entries) {
    const full = join(dir, e.name);
    if (e.isDirectory()) {
      out.push(...(await listEntriesRecursive(full, base)));
    } else {
      out.push(relative(base, full));
    }
  }
  return out;
};

const escapeTemplateLiteral = (content: string): string =>
  content.replaceAll("\\", "\\\\").replaceAll("`", "\\`").replaceAll("${", "\\${");

const run = async (): Promise<void> => {
  const layerBlocks: string[] = [];
  let totalFiles = 0;

  for (const { id: layerId } of LAYERS) {
    const abs = join(templatesDir, layerId);
    try {
      await readdir(abs);
    } catch {
      continue;
    }
    const rels = await listEntriesRecursive(abs, abs);
    const entries: string[] = [];
    for (const rel of [...rels].toSorted((a, b) => a.localeCompare(b))) {
      const fullPath = join(abs, rel);
      const outputKey = toLayerOutputKey(layerId, rel);
      const content = await readFile(fullPath, "utf-8");
      const escaped = escapeTemplateLiteral(content);
      entries.push(`    ["${outputKey}", \`${escaped}\`]`);
    }
    totalFiles += entries.length;
    layerBlocks.push(`  "${layerId}": new Map<string, string>([\n${entries.join(",\n")}\n  ])`);
  }

  const body = `/* eslint-disable max-lines */
/* This file is generated by scripts/generate-template-registry.ts — do not edit by hand. */

export const TEMPLATE_COUNT = ${String(totalFiles)} as const;

export const EMBEDDED_BY_LAYER = {
${layerBlocks.join(",\n")}
} as const satisfies Record<string, ReadonlyMap<string, string>>;
`;

  await writeFile(outFile, `${body}\n`, "utf-8");
};

await run();
